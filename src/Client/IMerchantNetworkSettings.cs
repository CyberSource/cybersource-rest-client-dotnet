using System;
using System.Collections.Generic;
using System.Net;
using System.Text;

namespace CyberSource.Client
{
    /// <summary>
    /// Defines the contract for merchant network settings used for HTTP communication configuration.
    /// Includes proxy settings, SSL/TLS configuration, timeout, connection pool settings, and request/response data.
    /// </summary>
    public interface IMerchantNetworkSettings
    {
        /// <summary>
        /// Gets the default developer ID to be included in API requests for tracking and attribution.
        /// </summary>
        string DefaultDeveloperId { get; }

        /// <summary>
        /// Gets a value indicating whether to use a proxy server for HTTP requests.
        /// </summary>
        string UseProxy { get; }

        /// <summary>
        /// Gets the address or hostname of the proxy server to use for HTTP requests.
        /// </summary>
        string ProxyAddress { get; }

        /// <summary>
        /// Gets the port number of the proxy server to use for HTTP requests.
        /// </summary>
        string ProxyPort { get; }

        /// <summary>
        /// Gets the username for authenticating with the proxy server, if required.
        /// </summary>
        string ProxyUsername { get; }

        /// <summary>
        /// Gets the password for authenticating with the proxy server, if required.
        /// </summary>
        string ProxyPassword { get; }

        /// <summary>
        /// Gets the configured WebProxy instance used for HTTP requests.
        /// </summary>
        WebProxy Proxy { get; }

        /// <summary>
        /// Gets the HTTP connection timeout in milliseconds.
        /// </summary>
        int TimeOut { get; }

        /// <summary>
        /// Gets the maximum number of connections to maintain in the connection pool.
        /// </summary>
        string MaxConnectionPoolSize { get; }

        /// <summary>
        /// Gets the time in seconds to keep idle connections alive before closing them.
        /// </summary>
        string KeepAliveTime { get; }

        /// <summary>
        /// Gets or sets the target endpoint or URI path for the CyberSource API request.
        /// This is typically the relative path to the API endpoint.
        /// </summary>
        string RequestTarget { get; set; }

        /// <summary>
        /// Gets the JSON-formatted request payload data to be sent in the API request body.
        /// </summary>
        string RequestJsonData { get; }

        /// <summary>
        /// Gets the type of HTTP request method to be used (e.g., POST, PUT, GET, PATCH).
        /// </summary>
        string RequestType { get; }

        /// <summary>
        /// Gets the SDK client identifier used to track SDK usage and version information in API requests.
        /// </summary>
        string SdkClientId { get; }

        /// <summary>
        /// Gets a value indicating whether the code was generated by MCP.
        /// When true, SDK telemetry headers (v-c-sdk-telemetry-merchant-id and v-c-sdk-telemetry-mcp) will be sent with API requests.
        /// </summary>
        bool IsSDK { get; }

        /// <summary>
        /// Gets or sets the web proxy for the request.
        /// </summary>
        /// <param name="proxy">The proxy object.</param>
        public void AddWebProxy(WebProxy proxy);
    }
}
