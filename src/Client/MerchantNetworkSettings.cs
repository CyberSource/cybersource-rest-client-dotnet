using AuthenticationSdk.util;
using NLog;
using System;
using System.Collections.Generic;
using System.Collections.Specialized;
using System.Configuration;
using System.Linq;
using System.Net;
using System.Reflection;

using NetworkKeys = AuthenticationSdk.core.MerchantConfigurationKeys.MerchantNetworkSettingsKeys;

namespace CyberSource.Client
{
    /// <summary>
    /// Provides merchant network settings for HTTP communication configuration.
    /// Handles proxy settings, SSL/TLS configuration, timeout, connection pool settings, and request/response data.
    /// Loads configuration from merchant settings or App.Config and initializes network-related properties.
    /// </summary>
    public class MerchantNetworkSettings : IMerchantNetworkSettings
    {
        #region Properties
        /// <summary>
        /// Gets or sets the default developer ID to be included in API requests for tracking and attribution.
        /// </summary>
        public string DefaultDeveloperId { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether to use a proxy server for HTTP requests.
        /// </summary>
        public string UseProxy { get; set; }

        /// <summary>
        /// Gets or sets the address or hostname of the proxy server to use for HTTP requests.
        /// </summary>
        public string ProxyAddress { get; set; }

        /// <summary>
        /// Gets or sets the port number of the proxy server to use for HTTP requests.
        /// </summary>
        public string ProxyPort { get; set; }

        /// <summary>
        /// Gets or sets the username for authenticating with the proxy server, if required.
        /// </summary>
        public string ProxyUsername { get; set; }

        /// <summary>
        /// Gets or sets the password for authenticating with the proxy server, if required.
        /// </summary>
        public string ProxyPassword { get; set; }

        /// <summary>
        /// Gets or sets the proxy object to use for the requests.
        /// </summary>
        /// <value>The proxy object.</value>
        public WebProxy Proxy { get; set; }

        /// <summary>
        /// Gets or sets the HTTP timeout (milliseconds). Default to 100000 milliseconds.
        /// </summary>
        /// <value>TimeOut.</value>
        public int TimeOut { get; set; }

        /// <summary>
        /// Gets or sets the maximum number of connections to maintain in the connection pool.
        /// </summary>
        public string MaxConnectionPoolSize { get; set; }

        /// <summary>
        /// Gets or sets the time in seconds to keep idle connections alive before closing them.
        /// </summary>
        public string KeepAliveTime { get; set; }

        /// <summary>
        /// Gets or sets the target endpoint or URI path for the CyberSource API request.
        /// This is typically the relative path to the API endpoint.
        /// </summary>
        private string _requestTarget;
        public string RequestTarget { get; set; }

        /// <summary>
        /// Gets or sets the JSON-formatted request payload data to be sent in the API request body.
        /// </summary>
        public string RequestJsonData { get; set; }

        /// <summary>
        /// Gets or sets the type of HTTP request method to be used (e.g., POST, PUT, GET, PATCH).
        /// </summary>
        public string RequestType { get; set; }

        /// <summary>
        /// Gets or sets the Client ID for SDK auditing and/or reporting.
        /// </summary>
        /// <value>The Client ID.</value>
        public string SdkClientId { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether the code was generated by MCP.
        /// When true, SDK telemetry headers will be sent with API requests.
        /// Default value: false.
        /// </summary>
        public bool IsSDK { get; set; }

        /// <summary>
        /// Gets or sets the NLog logger instance for logging network configuration and validation messages.
        /// </summary>
        public static Logger Logger { get; set; }
        #endregion Properties

        #region Constructors
        /// <summary>
        /// Initializes a new instance of the <see cref="MerchantNetworkSettings"/> class.
        /// Loads network configuration from merchant settings or App.Config and initializes all network-related properties.
        /// </summary>
        /// <param name="merchantLegacySettings">The legacy merchant settings containing network configuration data. If null, configuration is loaded from App.Config.</param>
        /// <exception cref="Exception">Thrown when configuration is invalid or cannot be accessed.</exception>
        public MerchantNetworkSettings(IReadOnlyDictionary<string, string> merchantNetworkDictionary = null)
        {
            if (Logger == null)
            {
                Logger = LogManager.GetCurrentClassLogger();
            }

            if (merchantNetworkDictionary == null)
            {
                IReadOnlyDictionary<string, string> merchantConfigDictionary = null;
                try
                {
                    var appSettingsSection = ConfigurationManager.GetSection("MerchantConfig") as NameValueCollection;
                    merchantConfigDictionary = appSettingsSection.AllKeys.ToDictionary(k => k, k => appSettingsSection[k]);
                    SetValuesFromDictionary(merchantConfigDictionary);
                }
                catch (ConfigurationErrorsException ex)
                {
                    Logger.Error($"Error accessing MerchantConfig section in App.Config: {ex.Message}");
                    throw new Exception($"{Constants.ErrorPrefix} Error accessing MerchantConfig section in App.Config: {ex.Message}", ex);
                }
                catch (Exception ex)
                {
                    Logger.Error($"Unexpected error accessing MerchantConfig section in App.Config: {ex.Message}");
                    throw new Exception($"{Constants.ErrorPrefix} Unexpected error accessing MerchantConfig section in App.Config: {ex.Message}", ex);
                }
            }
            else if (merchantNetworkDictionary != null)
            {
                SetValuesFromDictionary(merchantNetworkDictionary);
            }

            SdkClientId = GetClientId();
        }
        #endregion Constructors

        #region Methods
        /// <summary>
        /// Populates network configuration properties from the provided merchant configuration dictionary.
        /// Extracts and validates values for timeout, proxy settings, connection pool size, and keep-alive time.
        /// </summary>
        /// <param name="merchantConfigDictionary">A dictionary containing merchant network configuration key-value pairs.</param>
        /// <exception cref="Exception">Thrown when a required configuration key is not found.</exception>
        private void SetValuesFromDictionary(IReadOnlyDictionary<string, string> merchantConfigDictionary)
        {
            try
            {
                DefaultDeveloperId = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.DefaultDeveloperId, null);

                if (!merchantConfigDictionary.TryGetValue(NetworkKeys.TimeOut, out var timeoutValue) || !int.TryParse(timeoutValue, out int effectiveTimeout))
                {
                    TimeOut = int.Parse(Constants.DEFAULT_TIME_OUT);
                }
                else
                {
                    TimeOut = effectiveTimeout;
                }

                UseProxy = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.UseProxy, "false");

                ProxyAddress = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.ProxyAddress, null);

                ProxyPort = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.ProxyPort, null);

                ProxyUsername = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.ProxyUsername, null);

                ProxyPassword = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.ProxyPassword, null);

                if (UseProxy != null && bool.Parse(UseProxy))
                {
                    if (!string.IsNullOrWhiteSpace(ProxyAddress) && int.TryParse(ProxyPort, out int proxyPortTest))
                    {
                        WebProxy proxy = new WebProxy(ProxyAddress, proxyPortTest);

                        if (!string.IsNullOrWhiteSpace(ProxyUsername) && !string.IsNullOrWhiteSpace(ProxyPassword))
                        {
                            proxy.Credentials = new NetworkCredential(ProxyUsername, ProxyPassword);
                        }

                        AddWebProxy(proxy);
                    }
                }

                MaxConnectionPoolSize = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.MaxConnectionPoolSize, Constants.DefaultMaxConnectionPoolSize);

                KeepAliveTime = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.KeepAliveTime, Constants.DefaultKeepAliveTime);

                var isSDKValue = merchantConfigDictionary.GetValueOrDefault(NetworkKeys.IsSDK, "false");
                IsSDK = string.Equals(isSDKValue?.Trim(), "true", StringComparison.OrdinalIgnoreCase);
            }
            catch (KeyNotFoundException err)
            {
                Logger.Error($"Configuration key not found: {err.Message}");
                throw new Exception($"{Constants.ErrorPrefix} {err.Message}");
            }
        }

        /// <summary>
        /// Generates the SDK client identifier string for tracking and auditing purposes.
        /// Constructs an identifier in the format "cybs-rest-sdk-dotnet-{version}" using the assembly version.
        /// </summary>
        /// <returns>The SDK client identifier string including the SDK name and version.</returns>
        private string GetClientId()
        {
            var assembly = typeof(MerchantNetworkSettings).Assembly;

            var assemblyVersion = MyNewAssemblyHelper.GetAssemblyAttribute<AssemblyFileVersionAttribute>(assembly).Version;

            return "cybs-rest-sdk-dotnet-" + assemblyVersion;
        }

        /// <summary>
        /// Gets or sets the web proxy for the request.
        /// </summary>
        /// <param name="proxy">The proxy object.</param>
        public void AddWebProxy(WebProxy proxy)
        {
            Proxy = proxy;
        }
        #endregion Methods
    }

    /// <summary>
    /// Utility helper class for extracting custom attributes from assemblies.
    /// Provides extension methods to retrieve assembly-level attributes using reflection.
    /// </summary>
    public static class MyNewAssemblyHelper
    {
        /// <summary>
        /// Retrieves a custom attribute of the specified type from the assembly.
        /// </summary>
        /// <typeparam name="T">The type of attribute to retrieve, must be derived from <see cref="Attribute"/>.</typeparam>
        /// <param name="ass">The assembly to search for the attribute.</param>
        /// <returns>The first attribute of type T found on the assembly, or null if no such attribute exists.</returns>
        public static T GetAssemblyAttribute<T>(this Assembly ass) where T : Attribute
        {
            object[] attributes = ass.GetCustomAttributes(typeof(T), false);
            if (attributes == null || attributes.Length == 0)
                return null;
            return attributes.OfType<T>().SingleOrDefault();
        }
    }
}
